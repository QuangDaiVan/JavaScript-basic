// trong JavaScript, vòng lặp - loop là 1 cấu trúc điều khiển dùng để lặp lại 1 đoạn code  nhiều lần.
// vòng lặp thường được sử dụng khi cần thực hiện một hành động nhiều lần cho đến khi 1 điều kiện nào đó được thỏa mãn

// for: dùng khi biết trước số lần lặp

for (let i = 0; i <= 10; i++) {
    console.log(`count: ${i}`)
}
console.log('_______________')
for (let i = 10; i >= 0; i--) {
    console.log(`count: ${i}`)
}