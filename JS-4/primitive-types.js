// gồm các loại phổ biến như sau:
// Number: Đại diện cho các số, bao gồm cả số nguyên và số thập phân
// String: Đại diện cho các chuỗi ký tự
// Boolean: Đại diện cho các giá trị đúng - true hoặc sai - false
// Null: Đại diện cho 1 giá trị kiểu rỗng hoặc không có gì cả.
// Undefined: Đại diện cho 1 biến chưa được gán giá trị
// Symbol: Đại diện cho 1 giá trị độc nhất và bất biến, thường được sử dụng là khóa
// hoặc id cho các đối tượng - Objects
//BigInt: Đại diện cho số nguyên có giá trị rất lớn, lớn hơn kiểu Number thông thường bên trên
 